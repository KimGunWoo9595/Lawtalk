package com.userwrite.domain.vo;

public class UserWriteVO {

		private int requestNum;
		private int userNum;
		private int userWriteNum;
		private String userTitle;
		private String userContent;
		private String userWriteDate;
	
		public UserWriteVO() {;}

		public int getRequestNum() {
			return requestNum;
		}

		public void setRequestNum(int requestNum) {
			this.requestNum = requestNum;
		}

		public int getUserNum() {
			return userNum;
		}

		public void setUserNum(int userNum) {
			this.userNum = userNum;
		}

		public int getUserWriteNum() {
			return userWriteNum;
		}

		public void setUserWriteNum(int userWriteNum) {
			this.userWriteNum = userWriteNum;
		}

		public String getUserTitle() {
			return userTitle;
		}

		public void setUserTitle(String userTitle) {
			this.userTitle = userTitle;
		}

		public String getUserContent() {
			return userContent;
		}

		public void setUserContent(String userContent) {
			this.userContent = userContent;
		}

		public String getUserWriteDate() {
			return userWriteDate;
		}

		public void setUserWriteDate(String userWriteDate) {
			this.userWriteDate = userWriteDate;
		}
		
		
	
}
